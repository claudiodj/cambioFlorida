    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if((Float.parseFloat(String.valueOf(dolaresAComprar.getText())) * tipoCambio.valorCompra) <= inicioCaja.cajaPesos){
            inicioCaja.cajaPesos -= Float.parseFloat(String.valueOf(dolaresAComprar.getText())) * tipoCambio.valorCompra;
            inicioCaja.cajaDolares += Float.parseFloat(String.valueOf(dolaresAComprar.getText()));
            operaciones ventana = new operaciones();
            ventana.setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void dolaresAComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dolaresAComprarActionPerformed
        // TODO add your handling code here:
        System.out.println("Ha pulsado enter ventana Compra");
        float dolares = Float.parseFloat(String.valueOf(dolaresAComprar.getText()));
        pesosAPagar.setText(String.valueOf(dolares * tipoCambio.valorCompra));
        
        if(dolaresAComprar.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Debe ingresar un valor", "Acceso denegado", 
                    JOptionPane.ERROR_MESSAGE);
        }
        if((dolares * tipoCambio.valorCompra)>inicioCaja.cajaPesos){
            JOptionPane.showMessageDialog(null,"No tiene suficientes pesos para comprar", "Acceso denegado", 
                    JOptionPane.ERROR_MESSAGE);
        }
        
      //System.out.println(((JTextField)evt.getSource()).getText());
    }//GEN-LAST:event_dolaresAComprarActionPerformed

