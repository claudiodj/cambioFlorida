    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(dolaresAVender.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Debe ingresar Dolares a vender.", "Acceso denegado", 
                    JOptionPane.ERROR_MESSAGE);
        }else{
        if(Float.parseFloat(String.valueOf(dolaresAVender.getText())) < inicioCaja.cajaDolares){
            inicioCaja.cajaDolares -= Float.parseFloat(String.valueOf(dolaresAVender.getText()));
            inicioCaja.cajaPesos += Float.parseFloat(String.valueOf(dolaresAVender.getText())) * tipoCambio.valorVenta;
            operaciones ventana = new operaciones();
            ventana.setVisible(true);
            this.setVisible(false);
            }
        }

        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void dolaresAVenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dolaresAVenderActionPerformed
        // TODO add your handling code here:
        System.out.println("Ha pulsado enter ventana Venta");
        float dolares = Float.parseFloat(String.valueOf(dolaresAVender.getText()));
        pesosACobrar.setText(String.valueOf(dolares * tipoCambio.valorVenta));
        
        if(dolaresAVender.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Debe ingresar un valor", "Acceso denegado", 
                    JOptionPane.ERROR_MESSAGE);
        }
        if(dolares > inicioCaja.cajaDolares){
            JOptionPane.showMessageDialog(null,"No tiene suficientes dolares para vender", "Acceso denegado", 
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_dolaresAVenderActionPerformed

